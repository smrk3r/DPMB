def lookup_vendor(mac: str) -> str:
    """Look up the vendor name for a MAC address. Returns '' if unknown."""
    if not mac or len(mac) < 8:
        return ""
    oui = mac[:8].lower()
    return OUI_DB.get(oui, "")
